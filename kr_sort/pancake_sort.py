def pancake_sort(arr):
    n = len(arr)

    for curr_size in range(n, 1, -1):
        # Находим индекс максимального элемента в неотсортированной части
        max_idx = arr.index(max(arr[:curr_size]))

        # Если максимальный элемент не на своем месте
        if max_idx != curr_size - 1:
            # Переворачиваем массив до максимального элемента, чтобы он оказался в начале
            if max_idx != 0:
                arr[:max_idx + 1] = arr[:max_idx + 1][::-1]

            # Переворачиваем весь неотсортированный сегмент, чтобы максимальный элемент оказался на своем месте
            arr[:curr_size] = arr[:curr_size][::-1]

    return arr

# Пример сортировки
if __name__ == "__main__":
    array = [23, 10, 20, 11, 12, 6, 7]
    print("Исходный массив:", array)
    sorted_array = pancake_sort(array.copy())
    print("Отсортированный массив:", sorted_array)

'''
Исходный массив: [23, 10, 20, 11, 12, 6, 7]
Отсортированный массив: [6, 7, 10, 11, 12, 20, 23]
'''
