def bubble_sort(arr):
    """
    Сортировка пузырьком (Bubble Sort)
    """
    n = len(arr)

    # Проходим по всем элементам массива
    for i in range(n):
        # Флаг для оптимизации - если не было обменов, массив отсортирован
        swapped = False

        # Последние i элементов уже на своих местах, поэтому проходим до n-i-1
        for j in range(0, n - i - 1):
            # Сравниваем соседние элементы
            if arr[j] > arr[j + 1]:
                # Если текущий элемент больше следующего, меняем их местами
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
                swapped = True

        # Если на этой итерации не было обменов, массив отсортирован
        if not swapped:
            break

# Демонстрация работы функции
if __name__ == "__main__":
    # Тестовый массив
    numbers = [64, 34, 25, 12, 22, 11, 90]

    print("Исходный массив:")
    print(numbers)

    # Вызываем функцию сортировки
    bubble_sort(numbers)

    print("\nОтсортированный массив:")
    print(numbers)
