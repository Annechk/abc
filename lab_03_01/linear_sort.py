
def linear_search(arr, target):
    """
    Последовательный (линейный) поиск
    """
    # Проходим по всем элементам массива
    for i in range(len(arr)):
        # Если нашли целевой элемент, возвращаем его индекс
        if arr[i] == target:
            return i

    # Если элемент не найден, возвращаем -1
    return -1

# Демонстрация работы функции
if __name__ == "__main__":
    # Тестовый массив
    numbers = [64, 34, 25, 12, 22, 11, 90, 5, 77, 18]
    target = 22

    print("Массив для поиска:")
    print(numbers)
    print(f"Ищем элемент: {target}")

    # Вызываем функцию линейного поиска
    result = linear_search(numbers, target)

    # Выводим результат
    if result != -1:
        print(f"Элемент {target} найден на позиции {result}")
    else:
        print(f"Элемент {target} не найден в массиве")

