def shell_sort(arr):
    """
    Сортировка Шелла (Shell Sort)
    """
    n = len(arr)
    # Начинаем с большого промежутка, затем уменьшаем его
    gap = n // 2

    # Продолжаем, пока промежуток больше 0
    while gap > 0:
        # Выполняем сортировку вставками для этого промежутка
        for i in range(gap, n):
            # Сохраняем текущий элемент
            temp = arr[i]
            j = i

            # Сдвигаем элементы, которые больше temp, на позиции с шагом gap
            while j >= gap and arr[j - gap] > temp:
                arr[j] = arr[j - gap]
                j -= gap

            # Вставляем temp в правильную позицию
            arr[j] = temp

        # Уменьшаем промежуток
        gap //= 2

